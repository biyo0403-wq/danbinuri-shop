/** 규칙 기반 분류: [category, subCategory, cleanTitle] */
export function classify(goodsNo, name) {
  const n = name;
  const has = (s) => n.includes(s);

  let category = null;
  let subCategory = null;
  let clean = n;

  if (has("조끼")) {
    category = "vest";
    if (has("형광") || has("보안")) subCategory = "안전조끼";
    else if (has("밀리터리")) subCategory = "밀리터리";
    else if (has("특수조끼") || has("반사띠") || has("사다리")) subCategory = "특수다용도";
    else if (has("배색") && !has("타스란")) subCategory = "팀조끼";
    else subCategory = "기본형";
  } else if (has("티셔츠")) {
    category = "tshirt";
    if (has("라운드")) subCategory = "라운드";
    else if (has("지퍼")) subCategory = "기능성집업";
    else subCategory = "카라";
  } else if (has("제전복")) {
    category = "fleece";
    subCategory = "방진복";
  } else if (has("경비복") || has("스트라이프")) {
    category = "fleece";
    subCategory = "경비복";
  } else if (has("민방위")) {
    category = "fleece";
    subCategory = "민방위복";
  } else if (has("위생복") || has("약사가운")) {
    category = "fleece";
    subCategory = "병원복";
  } else if (has("작업가운")) {
    category = "fleece";
    subCategory = "가운";
  } else if (has("멜빵") || has("스즈끼") || has("일체형")) {
    category = "workwear";
    subCategory = "우주복/멜빵바지";
  } else if (has("세트")) {
    category = "workwear";
    subCategory = "상하 세트";
  } else if (has("바지")) {
    category = "workwear";
    subCategory = "바지";
  } else if (has("점퍼") || has("바람막이") || has("셔츠")) {
    category = "workwear";
    subCategory = "점퍼/자켓";
  }

  // 표시용 제목 정리: 브랜드/계절 문구/KSK,KP 코드 제거, 핵심 단어만 남긴다
  clean = clean
    .replace(/경신코리아/g, "")
    .replace(/봄\s*여름/g, "")
    .replace(/근무복\s*[·]?\s*작업복|근무복|작업복|산업체/g, "")
    // 코드가 문장 끝에 있는 경우 (일반적인 경우)
    .replace(/KSK-?[0-9A-Za-z,\.\-·\s]*$/g, "")
    .replace(/KP-?[0-9A-Za-z,\.\-·\s]*$/g, "")
    // 코드가 문장 맨 앞에 있는 경우 (예: "KP-40·681 TC 기능성...")
    .replace(/^(KSK|KP)-?[0-9A-Za-z,\.\-·]+\s+/g, "")
    .replace(/\s+/g, " ")
    .trim();

  if (!clean) clean = n;

  return { category, subCategory, title: clean };
}
